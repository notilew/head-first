// switch
const grade = 'C';

switch (grade) {
    case 'A':
        console.log('sua nota é um A.');
        break;
    case 'B':
        console.log('sua nota é um B.');
        break;
    case 'C':
        console.log('sua nota é um C.');
        break;
    case 'D':
        console.log('sua nota é um D.');
        break;
    case 'E':
        console.log('sua nota é um E.');
        break;
    default:
        console.log('nota inválida!');
}